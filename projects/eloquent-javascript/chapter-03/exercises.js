// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(numOne, numTwo) {
  if (numOne <= numTwo) {
    return numOne;
  } else {
    return numTwo;
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  if (num === 0) {
    return true;
  } else if (num === 1) {
    return false;
  } else if (num > 1) {
    return isEven(num - 2); 
  } else {
    return isEven(num + 2);
  }
}


